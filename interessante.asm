Title atividadee
.model small
.data
    MSG1 DB 10,13, "DIGITE A LETRA QUE DESEJA COMECAR: $"
    MSG2 DB 10,13,"DIGITE QUANTAS LETRAS DESEJA POSTERIORMENTE: $"
    MSG3 DB 10,13,"AS LETRAS SAO: $"
.code
Main proc
MOV AX,@DATA
MOV DS,AX

MOV AH,09
LEA DX,MSG1
INT 21h

MOV AH,01
int 21h
MOV BL,AL

MOV AH,09
LEA DX,MSG2
INT 21H

MOV AH,01
INT 21H
MOV CL,AL
AND CL,0FH

MOV AH,09
LEA DX,MSG3
INT 21h

MOV AH,02
MOV DL,BL
REPETE:
    INC DL
    CMP CL,0
    JE FIM
    INT 21H
    DEC CL 
    JMP REPETE

FIM:
MOV AH,4ch
int 21H
 MAIN ENDP
END MAIN